    private void btnPinjamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPinjamActionPerformed
        // TODO add your handling code here:
        String id = fid.getText();
        String nama = fnama.getText();
        String kode = fkode.getText();
        String tgl_pinjam = ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();
        String tgl_kembali = ((JTextField)jDateChooser2.getDateEditor().getUiComponent()).getText();
        if (!fid.getText().isEmpty() && !fnama.getText().isEmpty() && !fkode.getText().isEmpty()
            && !tgl_pinjam.isEmpty()&& !tgl_kembali.isEmpty())
        {
            rent.pinjam(id,nama,kode,tgl_pinjam,tgl_kembali);
        }
    }//GEN-LAST:event_btnPinjamActionPerformed

