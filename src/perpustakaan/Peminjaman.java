    private void load_tableBuku() {
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
    DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Kode Buku");
        dtm.addColumn("Judul");
        dtm.addColumn("Pengarang");
        dtm.addColumn("Penerbit");
        
        try {
            java.sql.Connection con; //METHOD MEMBUAT KONEKSI
            con = new KoneksiDatabase().connect();
            String sql = "SELECT * FROM `inputbuku`";
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery(sql);
            while (rs.next()){
                dtm.addRow(new Object[]{
                    rs.getString("kode"),
                    rs.getString("judul_buku"),
                    rs.getString("pengarang"),
                    rs.getString("penerbit"),
                    
                });
            }
            s.close();
            con.close();
           
            TableBuku.setModel(dtm);
        } catch (SQLException ex ) {
            Logger.getLogger(Peminjaman.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "You got an Error!", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
